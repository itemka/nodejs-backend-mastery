import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';

const SOURCE_PATH = '.mcp.json';
const TARGET_PATH = '.codex/config.toml';
const BEGIN_MARKER = '# BEGIN generated by pnpm sync-mcp';
const END_MARKER = '# END generated by pnpm sync-mcp';
const MCP_TABLE_PATTERN = /^\s*\[mcp_servers(?:\]|\.)/m;
const HELP_FLAGS = new Set(['--help', '-h']);
const HELP_TEXT = `
Usage:
  pnpm sync-mcp
  node scripts/sync-mcp-to-codex.mjs --help

Reads the local ${SOURCE_PATH} file and writes MCP server entries into ${TARGET_PATH}.

Requirements:
  - ${SOURCE_PATH} must exist in the repo root.
  - ${SOURCE_PATH} must contain a top-level "mcpServers" object.

Notes:
  - ${SOURCE_PATH} and ${TARGET_PATH} are ignored local files and may contain secrets.
  - Existing generated output is replaced between ${BEGIN_MARKER} and ${END_MARKER}.
  - Existing non-MCP Codex settings are preserved.
`.trim();

class ConfigError extends Error {
  name = 'ConfigError';
}

const fail = (message) => {
  throw new ConfigError(message);
};

const isObject = (value) => value && typeof value === 'object' && !Array.isArray(value);
// The MCP fields this script emits are string-oriented. Use a typed TOML writer if
// this script later grows numeric, boolean, or mixed-type config output.
const tomlString = (value) => JSON.stringify(String(value));
const tomlKey = (name) => (/[^a-zA-Z0-9_-]/.test(name) ? tomlString(name) : name);
const isEnoent = (error) => isObject(error) && error.code === 'ENOENT';

const readMcpServers = () => {
  if (!existsSync(SOURCE_PATH)) {
    fail(`Missing ${SOURCE_PATH}. Create the local MCP config before running pnpm sync-mcp.`);
  }

  let parsed;
  try {
    parsed = JSON.parse(readFileSync(SOURCE_PATH, 'utf8'));
  } catch (error) {
    if (isEnoent(error)) {
      fail(`Missing ${SOURCE_PATH}. Create the local MCP config before running pnpm sync-mcp.`);
    }

    fail(`Could not parse ${SOURCE_PATH}: ${error.message}`);
  }

  if (!isObject(parsed.mcpServers)) {
    fail(`${SOURCE_PATH} must contain an object at "mcpServers".`);
  }

  return parsed.mcpServers;
};

const buildObjectEntriesToml = (prefix, value, serverName) => {
  if (!isObject(value)) fail(`Server "${serverName}" ${prefix} must be an object.`);

  return Object.entries(value).map(([key, entryValue]) => {
    return `${prefix}.${tomlKey(key)} = ${tomlString(entryValue)}`;
  });
};

const buildServerToml = ([serverName, cfg]) => {
  if (!isObject(cfg)) fail(`Server "${serverName}" must be an object.`);
  if (cfg.command && cfg.url) fail(`Server "${serverName}" cannot define both command and url.`);
  if (cfg.args && !Array.isArray(cfg.args)) fail(`Server "${serverName}" args must be an array.`);

  const lines = [`[mcp_servers.${tomlKey(serverName)}]`];

  if (cfg.command) lines.push(`command = ${tomlString(cfg.command)}`);
  if (cfg.args) {
    lines.push(`args = [${cfg.args.map((argument) => tomlString(argument)).join(', ')}]`);
  }
  if (cfg.url) lines.push(`url = ${tomlString(cfg.url)}`);
  if (cfg.headers) lines.push(...buildObjectEntriesToml('http_headers', cfg.headers, serverName));
  if (cfg.env) lines.push(...buildObjectEntriesToml('env', cfg.env, serverName));

  return `${lines.join('\n')}\n`;
};

const buildGeneratedBlock = (mcpServers) =>
  [
    BEGIN_MARKER,
    `# Source: ${SOURCE_PATH}. Do not edit this block by hand.`,
    '[mcp_servers]',
    '',
    ...Object.entries(mcpServers).map((entry) => buildServerToml(entry)),
    END_MARKER,
    '',
  ].join('\n');

const isOnlyMcpConfig = (currentConfig) => {
  let inMcpTable = false;

  for (const line of currentConfig.split(/\r?\n/)) {
    const trimmedLine = line.trim();
    if (!trimmedLine || trimmedLine.startsWith('#')) continue;

    if (trimmedLine.startsWith('[')) {
      inMcpTable = MCP_TABLE_PATTERN.test(trimmedLine);
      if (!inMcpTable) return false;
      continue;
    }

    if (!inMcpTable) return false;
  }

  return true;
};

const replaceGeneratedBlock = (currentConfig, generatedBlock) => {
  const hasBegin = currentConfig.includes(BEGIN_MARKER);
  const hasEnd = currentConfig.includes(END_MARKER);

  if (hasBegin !== hasEnd) {
    fail(`${TARGET_PATH} has an incomplete pnpm sync-mcp generated block.`);
  }

  if (hasBegin) {
    const blockPattern = new RegExp(String.raw`${BEGIN_MARKER}[\s\S]*?${END_MARKER}\n?`);
    return currentConfig.replace(blockPattern, generatedBlock);
  }

  if (!currentConfig.trim()) return generatedBlock;
  if (MCP_TABLE_PATTERN.test(currentConfig) && isOnlyMcpConfig(currentConfig))
    return generatedBlock;

  if (MCP_TABLE_PATTERN.test(currentConfig)) {
    fail(
      `${TARGET_PATH} already contains MCP server config without pnpm sync-mcp markers. Move manual MCP entries into ${SOURCE_PATH}, remove the old MCP block, or add generated markers before rerunning.`,
    );
  }

  return `${currentConfig.replace(/\s*$/, '')}\n\n${generatedBlock}`;
};

const writeCodexConfig = (generatedBlock) => {
  const currentConfig = existsSync(TARGET_PATH) ? readFileSync(TARGET_PATH, 'utf8') : '';
  const nextConfig = replaceGeneratedBlock(currentConfig, generatedBlock);

  mkdirSync('.codex', { recursive: true });
  writeFileSync(TARGET_PATH, nextConfig);
};

if (process.argv.slice(2).some((argument) => HELP_FLAGS.has(argument))) {
  console.log(HELP_TEXT);
} else {
  try {
    writeCodexConfig(buildGeneratedBlock(readMcpServers()));
    console.log(`Synced ${SOURCE_PATH} to ${TARGET_PATH}`);
  } catch (error) {
    const message = error instanceof Error ? error.message : String(error);
    console.error(`sync-mcp failed: ${message}`);
    process.exitCode = 1;
  }
}
